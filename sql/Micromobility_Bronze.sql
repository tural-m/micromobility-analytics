CREATE OR REPLACE TABLE MICROMOBILITY_DB.BRONZE.TRIPS_RAW(
    TRIP_ID                  VARCHAR,
    DEVICE_ID                VARCHAR,
    VEHICLE_TYPE             VARCHAR,
    TRIP_DURATION            VARCHAR,
    TRIP_DISTANCE            VARCHAR,
    START_TIME               VARCHAR,
    END_TIME                 VARCHAR,
