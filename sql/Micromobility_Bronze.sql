    MONTH                    VARCHAR,
    HOUR                     VARCHAR,
    DAY_OF_WEEK              VARCHAR,
    COUNCIL_DISTRICT_START   VARCHAR,
    COUNCIL_DISTRICT_END     VARCHAR,
    YEAR                     VARCHAR,
    CENSUS_TRACT_START       VARCHAR,
    CENSUS_TRACT_END         VARCHAR,
    START_TIME_CENTRAL       VARCHAR,
    END_TIME_CENTRAL         VARCHAR,
    INGESTION_TIMESTAMP      TIMESTAMP DEFAULT CURRENT_TIMESTAMP(),
    SOURCE_FILE              VARCHAR DEFAULT 'micromobility_raw.csv'    
);

CREATE OR REPLACE STAGE MICROMOBILITY_DB.BRONZE.RAW_STAGE
FILE_FORMAT = (
    TYPE = 'CSV'
    FIELD_OPTIONALLY_ENCLOSED_BY = '"'
    SKIP_HEADER = 1
    NULL_IF = ('NULL', 'null', '', 'N/A')
    EMPTY_FIELD_AS_NULL = TRUE
    TRIM_SPACE = TRUE
);

LIST @MICROMOBILITY_DB.BRONZE.RAW_STAGE;

COPY INTO MICROMOBILITY_DB.BRONZE.TRIPS_RAW (
    TRIP_ID,
    DEVICE_ID,
    VEHICLE_TYPE,
    TRIP_DURATION,
    TRIP_DISTANCE,
    START_TIME,
    END_TIME,
    MODIFIED_DATE,
    MONTH,
    HOUR,
    DAY_OF_WEEK,
    COUNCIL_DISTRICT_START,
    COUNCIL_DISTRICT_END,
    YEAR,
    CENSUS_TRACT_START,
    CENSUS_TRACT_END,
    START_TIME_CENTRAL,
    END_TIME_CENTRAL
)
FROM @MICROMOBILITY_DB.BRONZE.RAW_STAGE
FILE_FORMAT = (
    TYPE = 'CSV'
    FIELD_OPTIONALLY_ENCLOSED_BY = '"'
    SKIP_HEADER = 1
    NULL_IF = ('NULL', 'null', '', 'N/A')
    EMPTY_FIELD_AS_NULL = TRUE
    TRIM_SPACE = TRUE
)
ON_ERROR = 'CONTINUE'
PURGE = FALSE;

SELECT COUNT(*) AS TOTAL_ROWS 
FROM MICROMOBILITY_DB.BRONZE.TRIPS_RAW;

SELECT YEAR, COUNT(*) AS TRIPS
FROM MICROMOBILITY_DB.BRONZE.TRIPS_RAW
GROUP BY YEAR
ORDER BY YEAR;

SELECT 
    REPLACE(YEAR, ',', '') AS YEAR_CLEAN,
    CAST(MONTH AS INTEGER) AS MONTH_NUM,
    COUNT(*) AS TRIPS
FROM MICROMOBILITY_DB.BRONZE.TRIPS_RAW
WHERE REPLACE(YEAR, ',', '') IN ('2021', '2022')
GROUP BY YEAR_CLEAN, MONTH_NUM
ORDER BY YEAR_CLEAN, MONTH_NUM;

SELECT 
    VEHICLE_TYPE,
    REPLACE(YEAR, ',', '') AS YEAR_CLEAN,
    COUNT(*) AS TRIPS,
    ROUND(COUNT(*) * 100.0 / SUM(COUNT(*)) OVER(PARTITION BY REPLACE(YEAR, ',', '')), 2) AS PCT_OF_YEAR
FROM MICROMOBILITY_DB.BRONZE.TRIPS_RAW
WHERE REPLACE(YEAR, ',', '') IN ('2021', '2022')
GROUP BY VEHICLE_TYPE, YEAR_CLEAN
ORDER BY YEAR_CLEAN, TRIPS DESC;

